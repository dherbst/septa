// a command line tool to use the septa API to get information about SEPTA transportation.
//
// septa is the cli for the septa API.
//
// OPTIONS
//
// alerts [route] - display alerts
//
// next src dest - show the next to arrive information between src and dest stations.
//
// stations - lists the stations.
//
// version - display the version of the septa cli.
//
// INSTALL
//
// Use the go command to install:
//
//    $ go install github.com/dherbst/septa/cmd/septa@latest
//
